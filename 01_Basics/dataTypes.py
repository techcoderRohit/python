# numbers data type = integers, float, complex

a = 5
b = 4.5
c = 2 + 4j
print(type(a))
print(type(b))
print(type(c))

#sequence data type = string , list , tuple
#string
s = "Welcome to the world"
print(s)
print(type(s))

#list
l = [1,2,3]
print(l)
print(type(l))

l1 = ["apple", "banana", "mango"]
print(l1)
print(type(l1))

#tuple
t = (1,)
print(type(t))

t1 = ('python', 'java' , 1 ,"c")
print(t1)
print(type(t1))

#boolean data type

print(type(True))
print(type(False))

#set data type

s1 = {"a", "b", "c", "d", "b", "c", "a"}
print(s1)
print(type(s1))

#dictionary data type

d = {1: 'rohit' , 2:'raj', 3:'ashu'}
print(d)
print(type(d))

